// Category configuration
const CATEGORIES = [
  { id: 'recent', icon: Clock, label: 'Recent' },
  { id: 'smileys', icon: Smile, label: 'Smileys' },
  { id: 'people', icon: User, label: 'People' },
  { id: 'animals', icon: Dog, label: 'Animals' },
  { id: 'food', icon: Coffee, label: 'Food' },
  { id: 'travel', icon: Car, label: 'Travel' },
  { id: 'activities', icon: Gamepad2, label: 'Activities' },
  { id: 'objects', icon: Lightbulb, label: 'Objects' },
  { id: 'symbols', icon: Heart, label: 'Symbols' },
];

const RECENT_STORAGE_KEY = 'swarm-recent-emojis';
const MAX_RECENT = 24;

interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  onClose: () => void;
}

export function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [search, setSearch] = useState('');
  const [activeCategory, setActiveCategory] = useState('smileys');
  const [recentEmojis, setRecentEmojis] = useState<string[]>([]);

  // Load recent emojis from localStorage
  useEffect(() => {
    try {
      const stored = localStorage.getItem(RECENT_STORAGE_KEY);
      if (stored) {
        setRecentEmojis(JSON.parse(stored));
      }
    } catch {
      // Ignore localStorage errors
    }
  }, []);

  // Save emoji to recent
  const addToRecent = useCallback((emoji: string) => {
    setRecentEmojis((prev) => {
      const filtered = prev.filter((e) => e !== emoji);
      const updated = [emoji, ...filtered].slice(0, MAX_RECENT);
      try {
        localStorage.setItem(RECENT_STORAGE_KEY, JSON.stringify(updated));
      } catch {
        // Ignore localStorage errors
      }
      return updated;
    });
  }, []);

  // Handle emoji selection
  const handleSelect = useCallback((emoji: string) => {
    addToRecent(emoji);
    onSelect(emoji);
    onClose();
  }, [addToRecent, onSelect, onClose]);

  // Filter emojis by search
  const filteredEmojis = useMemo(() => {
    if (!search.trim()) {
      return null; // Show categories when not searching
    }

    // Search across all categories
    const allEmojis = Object.values(EMOJI_DATA).flat();
    // For emoji search, we just return all since emojis don't have text names
    // A proper implementation would have emoji names/keywords
    return allEmojis;
  }, [search]);

  // Get emojis for current category
  const categoryEmojis = useMemo(() => {
    if (activeCategory === 'recent') {
      return recentEmojis;
    }
    return EMOJI_DATA[activeCategory] || [];
  }, [activeCategory, recentEmojis]);

  return (
    <div className="w-80 max-h-[480px] bg-slate-800 rounded-xl shadow-2xl border border-slate-600 overflow-hidden flex flex-col">
      {/* Search */}
      <div className="p-2 border-b border-slate-700">
        <div className="relative">
          <Search className="absolute left-2.5 top-1/2 -translate-y-1/2 w-4 h-4 text-gray-400" />
          <input
            type="text"
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            placeholder="Search emojis..."
            className="w-full pl-8 pr-3 py-1.5 bg-slate-700 border border-slate-600 rounded-lg text-sm text-white placeholder-gray-400 focus:outline-none focus:border-blue-500"
            autoFocus
          />
        </div>
      </div>

      {/* Category tabs */}
      {!search && (
        <div className="flex border-b border-slate-700 px-1 py-1 gap-0.5 overflow-x-auto scrollbar-none">
          {CATEGORIES.map((cat) => {
            const Icon = cat.icon;
            const isActive = activeCategory === cat.id;
            const hasRecent = cat.id !== 'recent' || recentEmojis.length > 0;

            if (!hasRecent) return null;

            return (
              <button
                key={cat.id}
                onClick={() => setActiveCategory(cat.id)}
                className={cn(
                  'p-1.5 rounded-lg transition-colors flex-shrink-0',
                  isActive
                    ? 'bg-blue-600 text-white'
                    : 'text-gray-400 hover:text-white hover:bg-slate-700'
                )}
                title={cat.label}
              >
                <Icon className="w-4 h-4" />
              </button>
            );
          })}
        </div>
      )}

      {/* Emoji grid */}
      <div className="flex-1 overflow-y-auto p-2 scrollbar-thin scrollbar-thumb-slate-600 scrollbar-track-transparent">
        {search ? (
          // Search results
          <div className="grid grid-cols-8 gap-0.5">
            {filteredEmojis?.slice(0, 200).map((emoji, idx) => (
              <button
                key={`${emoji}-${idx}`}
                onClick={() => handleSelect(emoji)}
                className="w-8 h-8 flex items-center justify-center text-xl hover:bg-slate-700 rounded-lg transition-colors"
              >
                {emoji}
              </button>
            ))}
          </div>
        ) : (
          // Category emojis
          <>
            {activeCategory === 'recent' && recentEmojis.length === 0 ? (
              <div className="text-center text-gray-400 text-sm py-8">
                No recent emojis yet
              </div>
            ) : (
              <div className="grid grid-cols-8 gap-0.5">
                {categoryEmojis.map((emoji, idx) => (
                  <button
                    key={`${emoji}-${idx}`}
                    onClick={() => handleSelect(emoji)}
                    className="w-8 h-8 flex items-center justify-center text-xl hover:bg-slate-700 rounded-lg transition-colors"
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            )}
          </>
        )}
      </div>

      {/* Footer */}
      <div className="px-3 py-1.5 border-t border-slate-700 text-xs text-gray-500 text-center">
        {activeCategory !== 'recent' && !search && (
          <span>{CATEGORIES.find(c => c.id === activeCategory)?.label} â€¢ {categoryEmojis.length} emojis</span>
        )}
        {activeCategory === 'recent' && !search && (
          <span>Recently used</span>
        )}
        {search && (
          <span>Search results</span>
        )}
      </div>
    </div>
  );
}

export default EmojiPicker;
